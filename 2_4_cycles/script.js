// Задание 3: Подсчет суммы всех четных чисел от 0 до n
// Инструкция: Напишите цикл for , который будет считать
// сумму всех четных чисел от 0 до n .
// Выведите результат.
let n = 13; // Задайте значение n
let sum = 0;
// Напишите цикл здесь
for (let i = 0; i <= n; i++) {
  if (i % 2 === 0) {
    sum += i;
  }
}
console.log(`Сумма всех четных чисел от 0 до ${n} равна ${sum}`);

// Задание 4: Подсчет количества совпадений подстроки
// Инструкция: Напишите код, который будет подсчитывать
// количество вхождений указанной подстроки в строке.
// Используйте цикл для поиска подстроки и увеличивайте
// счетчик при каждом совпадении.
let str = "Hello, how are you? Hello again!"; // Исходная строка
let subStr = "Hello"; // Подстрока для поиска
let count = 0;
let pos = str.indexOf(subStr);
// Напишите цикл здесь
while (pos !== -1) {
  count++;
  pos = str.indexOf(subStr, pos + 1);
}
console.log(`Количество вхождений подстроки "${subStr}" в строке: ${count}`);

